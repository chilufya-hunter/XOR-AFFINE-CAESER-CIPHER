# Исключающее или двух двоичных строк

# Функция для поиска
# Исключающее или двух двоичных строк
def xor(a, b, n): 
	MON = "" 
	
	# Цикл для итерации по
# двоичная строка
	for i in range(n): 
		
		# Если символ совпадает 
		if (a[i] == b[i]): 
			MON += "0"
		else: 
			MON += "1"
	return MON
# ввод двоичных чисел в виде строки
if __name__ == "__main__": 
	a=input("ВВЕДИТЕ ПЕРВУЮ СТРОКУ ДВОИЧНЫХ ЧИСЕЛ: ")
	b=input("ВВЕДИТЕ ВТОРУЮ СТРОКУ ДВОИЧНЫХ ЧИСЕЛ: ")

	n = len(a) 
	c = xor(a, b, n) 
	print("РЕЗУЛЬТАТОМ ПРИВЕДЕННЫХ ВЫШЕ ДВОИЧНЫХ ЧИСЕЛ: "+c) 
